package day0215;

/**
 *	구현하는 인터페이스가 상속관계를 가지고 있을 때, 구현 클래스에서는 모든 abstract method를 Override 해야한다.
 * @author SIST
 */
public class SubClassD implements SuperInterC{
	@Override
	public void methodA() { //SuperInterA abstract method
		
	}

	@Override
	public void methodB() { //SuperInterB abstract method
		
	}
	
	@Override
	public void methodB1() { //SuperInterB1 abstract method
		
	}
	
	@Override
	public void methodC() { //SuperInterC abstract method
		
	}
	
	
}
